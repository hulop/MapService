package hulop.hokoukukan.servlet;

import java.io.IOException;

import javax.servlet.ServletException;
import javax.servlet.annotation.WebServlet;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

import org.apache.wink.json4j.JSON;
import org.apache.wink.json4j.JSONObject;

import hulop.hokoukukan.bean.AgreementBean;

/**
 * Servlet implementation class EnqueteServlet
 */
@WebServlet("/api/enquete")
public class EnqueteServlet extends HttpServlet {
	private static final long serialVersionUID = 1L;
	private final AgreementBean bean = new AgreementBean();

	/**
	 * @see HttpServlet#doGet(HttpServletRequest request, HttpServletResponse
	 *      response)
	 */
	protected void doGet(HttpServletRequest request, HttpServletResponse response)
			throws ServletException, IOException {
		request.setCharacterEncoding("UTF-8");
		String answers = request.getParameter("answers");
		if (answers != null) {
			try {
				bean.setEntry((JSONObject) JSON.parse(answers));
				return;
			} catch (Exception e) {
				e.printStackTrace();
			}
		}
		response.sendError(HttpServletResponse.SC_BAD_REQUEST);
	}

	/**
	 * @see HttpServlet#doPost(HttpServletRequest request, HttpServletResponse
	 *      response)
	 */
	protected void doPost(HttpServletRequest request, HttpServletResponse response)
			throws ServletException, IOException {
		doGet(request, response);
	}

}
